package com.raphfrk.craftproxylite;

public class ClientInfo {
	
	private String username;
	private String ip;
	
	public void setUsername(String username) {
		this.username = username;
	}
	
	public String getUsername() {
		return username;
	}
	
	public void setIP(String username) {
		this.ip = username;
	}
	
	public String getIP() {
		return ip;
	}

}
